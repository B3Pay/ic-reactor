/**
 * Codegen Pipeline
 *
 * Orchestrates all generators for a single canister.
 * This is the primary entry point used by @ic-reactor/cli and @ic-reactor/vite-plugin.
 *
 * Pipeline steps (in order):
 *  1. Resolve paths (didFile, outDir)
 *  2. Generate the merged TypeScript-only `generated.ts`
 *     (typed service types + real idlFactory/init, and — when enabled — the
 *     inlined reactor + hook exports)
 *  3. Optionally create or migrate the user entry (`index.ts`)
 *
 * V0 of the Candid compiler/codegen evolution: no parallel `.js`/`.d.ts` and no
 * copied `.did` are emitted. The source `.did` remains the codegen input.
 */

import fs from "node:fs"
import path from "node:path"
import type {
  CanisterConfig,
  CodegenConfig,
  CodegenTarget,
  GeneratorResult,
  ReactorClassName,
} from "./types.js"
import {
  generateEntryFile,
  generateGeneratedFile,
} from "./generators/declarations.js"

export interface PipelineOptions {
  /** Canister name and config */
  canisterConfig: CanisterConfig
  /**
   * Absolute path to the project root.
   * Relative paths in `CanisterConfig` are resolved from here.
   */
  projectRoot: string
  /**
   * Global codegen config (for fallback outDir and clientManagerPath).
   */
  globalConfig: Pick<CodegenConfig, "outDir" | "clientManagerPath" | "target">
  /**
   * Whether the managed reactor files should be generated.
   * Defaults to true.
   */
  generateReactor?: boolean
}

function resolveReactorClass(canisterConfig: CanisterConfig): ReactorClassName {
  return canisterConfig.mode ?? "DisplayReactor"
}

function resolveRuntimeTarget(
  canisterConfig: CanisterConfig,
  globalConfig: Pick<CodegenConfig, "outDir" | "clientManagerPath" | "target">
): CodegenTarget {
  return canisterConfig.target ?? globalConfig.target ?? "react"
}

function normalizeFileContent(content: string): string {
  return content.replace(/\r\n/g, "\n").trim()
}

function isLegacyGeneratedIndexFile(content: string): boolean {
  // Old fully generated reactor indexes were managed output, not user-owned
  // wrappers. They can be replaced by the new stable wrapper.
  return (
    content.includes("Auto-generated by @ic-reactor/codegen") &&
    content.includes("createActorHooks(")
  )
}

function generateLegacyEntryFile(): string {
  return `/**
 * Canister entrypoint.
 *
 * Created once by @ic-reactor/codegen and safe to customize.
 * Keep the re-export below if you want generated exports and types to stay in sync.
 *
 * Recommended customization points:
 * - define reusable query/mutation factories
 * - add app-specific hooks and cache invalidation wiring
 * - compose generated APIs into route loaders/actions
 *
 * Do not edit \`index.generated.ts\`; it is regenerated on each codegen run.
 * AI guide: https://ic-reactor.b3pay.net/llms-full.txt
 * Skill install: npx skills add B3Pay/ic-reactor-skills --full-depth --skill ic-reactor-hooks
 */
export * from "./index.generated"
`
}

function isLegacyManagedEntryWrapper(content: string): boolean {
  return (
    normalizeFileContent(content) ===
    normalizeFileContent(generateLegacyEntryFile())
  )
}

function migrateLegacyEntryWrapperExport(content: string): string | null {
  const migrated = content.replace(
    /export\s+\*\s+from\s+["']\.\/index\.generated["']/g,
    'export * from "./generated"'
  )

  return migrated === content ? null : migrated
}

function isCurrentOrLegacyManagedEntryWrapper(
  content: string,
  expectedEntryContent: string
): boolean {
  return (
    isManagedEntryWrapper(content, expectedEntryContent) ||
    isLegacyManagedEntryWrapper(content)
  )
}

function isManagedEntryWrapper(
  content: string,
  expectedEntryContent: string
): boolean {
  return (
    normalizeFileContent(content) === normalizeFileContent(expectedEntryContent)
  )
}

export interface PipelineResult {
  canisterName: string
  success: boolean
  /** All files the pipeline attempted to write */
  files: GeneratorResult[]
  error?: string
}

/**
 * Run the full codegen pipeline for a single canister.
 *
 * @returns PipelineResult with per-file details
 */
export async function runCanisterPipeline(
  options: PipelineOptions
): Promise<PipelineResult> {
  const {
    canisterConfig,
    projectRoot,
    globalConfig,
    generateReactor = true,
  } = options
  const { name, didFile, clientManagerPath } = canisterConfig

  const files: GeneratorResult[] = []

  // ── Resolve paths ──────────────────────────────────────────────────────────

  const resolvedDidFile = path.isAbsolute(didFile)
    ? didFile
    : path.resolve(projectRoot, didFile)

  if (!fs.existsSync(resolvedDidFile)) {
    return {
      canisterName: name,
      success: false,
      files: [],
      error: `DID file not found: ${resolvedDidFile}`,
    }
  }

  // Per-canister outDir overrides global outDir
  const canisterOutDir =
    canisterConfig.outDir != null
      ? path.isAbsolute(canisterConfig.outDir)
        ? canisterConfig.outDir
        : path.resolve(projectRoot, canisterConfig.outDir)
      : path.resolve(projectRoot, globalConfig.outDir, name)

  // clientManagerPath falls back to global, then a safe default
  const resolvedClientManagerPath =
    clientManagerPath ?? globalConfig.clientManagerPath ?? "../../clients"

  const reactorClass = resolveReactorClass(canisterConfig)
  const runtimeTarget = resolveRuntimeTarget(canisterConfig, globalConfig)

  // ── Step 1: Generated file ───────────────────────────────────────────────

  const generatedPath = path.join(canisterOutDir, "generated.ts")

  try {
    const generatedResult = generateGeneratedFile({
      canisterName: name,
      didFile: resolvedDidFile,
      didContent: fs.readFileSync(resolvedDidFile, "utf-8"),
      includeReactor: generateReactor,
      canisterId: canisterConfig.canisterId,
      clientManagerPath: resolvedClientManagerPath,
      runtimeTarget,
      reactorClass,
    })

    if (!generatedResult.success) {
      return {
        canisterName: name,
        success: false,
        files,
        error: generatedResult.error,
      }
    }

    fs.mkdirSync(canisterOutDir, { recursive: true })
    fs.writeFileSync(generatedPath, generatedResult.content)
    files.push({ success: true, filePath: generatedPath })
  } catch (err) {
    files.push({
      success: false,
      filePath: generatedPath,
      error: `Generated file step failed: ${err instanceof Error ? err.message : String(err)}`,
    })
    return {
      canisterName: name,
      success: false,
      files,
      error: `Generated file step failed: ${err instanceof Error ? err.message : String(err)}`,
    }
  }

  if (!generateReactor) {
    return {
      canisterName: name,
      success: true,
      files,
    }
  }

  // ── Step 2: User-owned entry wrapper ─────────────────────────────────────

  const entryPath = path.join(canisterOutDir, "index.ts")
  const entryContent = generateEntryFile()

  try {
    if (!fs.existsSync(entryPath)) {
      fs.writeFileSync(entryPath, entryContent)
      files.push({ success: true, filePath: entryPath })
    } else {
      const existingEntryContent = fs.readFileSync(entryPath, "utf-8")
      const migratedEntryContent =
        migrateLegacyEntryWrapperExport(existingEntryContent)

      if (
        isLegacyGeneratedIndexFile(existingEntryContent) ||
        isCurrentOrLegacyManagedEntryWrapper(existingEntryContent, entryContent)
      ) {
        fs.writeFileSync(entryPath, entryContent)
        files.push({ success: true, filePath: entryPath })
      } else if (migratedEntryContent != null) {
        fs.writeFileSync(entryPath, migratedEntryContent)
        files.push({ success: true, filePath: entryPath })
      } else {
        files.push({ success: true, filePath: entryPath, skipped: true })
      }
    }
  } catch (err) {
    files.push({
      success: false,
      filePath: entryPath,
      error: `Entry wrapper step failed: ${err instanceof Error ? err.message : String(err)}`,
    })

    return {
      canisterName: name,
      success: false,
      files,
      error: `Entry wrapper step failed: ${err instanceof Error ? err.message : String(err)}`,
    }
  }

  return {
    canisterName: name,
    success: true,
    files,
  }
}
