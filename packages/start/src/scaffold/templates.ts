/** `backend/main.mo` — a tiny counter canister that demonstrates query/update. */
export function backendMainMoTemplate(): string {
  return `import Array "mo:base/Array";
import Principal "mo:base/Principal";

persistent actor {
  // V0 starter backend: a simple counter.
  // Replace this with your own canister logic. Run "icp deploy" to publish.

  stable var count : Nat = 0;
  stable var owners : [Principal] = [];

  public query func get_count() : async Nat {
    return count;
  };

  public shared (msg) func increment() : async Nat {
    count += 1;
    owners := Array.append(owners, [msg.caller]);
    return count;
  };

  public shared (msg) func reset() : async Nat {
    count := 0;
    return count;
  };

  public query func who_called() : async [Principal] {
    return owners;
  };
};
`
}

/** `backend/backend.did` — Candid interface for the starter counter canister. */
export function backendDidTemplate(): string {
  return `service : {
  get_count: () -> (nat) query;
  increment: () -> (nat);
  reset: () -> (nat);
  who_called: () -> (vec principal) query;
}
`
}

/**
 * Root `ic-reactor.json` — configures the backend canister with its `.did`.
 * Consumed by the CLI; the Vite plugin reads config from vite.config.ts.
 */
export function icReactorJsonTemplate(): string {
  return (
    JSON.stringify(
      {
        $schema: "./node_modules/@ic-reactor/codegen/schema.json",
        outDir: "frontend/src/declarations",
        clientManagerPath: "../../lib/client",
        target: "react",
        canisters: {
          backend: {
            name: "backend",
            didFile: "./backend/backend.did",
            clientManagerPath: "../../lib/client",
          },
        },
      },
      null,
      2
    ) + "\n"
  )
}

