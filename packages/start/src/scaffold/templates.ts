/**
 * @ic-reactor/start — Scaffold file templates
 *
 * Pure string-template functions for the default V0 generated app. These are
 * kept separate from the filesystem-writing `createApp` so they can be unit
 * tested without touching disk.
 *
 * Conventions for the default layout:
 *
 * ```text
 * <app>/
 *   icp.yaml
 *   ic-reactor.json
 *   backend/
 *     canister.yaml
 *     main.mo
 *     backend.did
 *   frontend/
 *     canister.yaml
 *     package.json
 *     vite.config.ts
 *     tsconfig.json
 *     index.html
 *     src/
 *       main.tsx
 *       styles.css
 *       routeTree.gen.ts        (committed; regenerated by TanStack Router)
 *       routes/__root.tsx
 *       routes/index.tsx
 *       lib/client.ts
 *       lib/auth.ts
 * ```
 *
 * Canister IDs are intentionally never baked into templates. They resolve at
 * runtime via `icp-cli` + the `ic_env` cookie injected by
 * `@ic-reactor/vite-plugin`.
 */

export interface TemplateContext {
  /** npm package name of the app, e.g. "my-app". */
  appName: string
}

const ICP_YAML_SCHEMA_REF =
  "https://github.com/dfinity/icp-cli/raw/refs/tags/v0.1.0-beta.2/docs/schemas/icp-yaml-schema.json"
const CANISTER_YAML_SCHEMA_REF =
  "https://github.com/dfinity/icp-cli/raw/refs/tags/v0.1.0-beta.2/docs/schemas/canister-yaml-schema.json"

/** Root `icp.yaml` — managed local network with II enabled. */
export function icpYamlTemplate(): string {
  return `# yaml-language-server: $schema=${ICP_YAML_SCHEMA_REF}

# ICP CLI project configuration for an @ic-reactor/start app.
# Start a local replica:   icp network start -d
# Deploy all canisters:    icp deploy
# Sync frontend assets:    icp sync
# Deploy to mainnet:       icp deploy -e ic

canisters:
  - backend
  - frontend

networks:
  - name: local
    mode: managed
    ii: true
    gateway:
      port: 8000
`
}

