/** `frontend/src/routes/index.tsx` — home route with a sample backend query/update + II login. */
export function frontendIndexRouteTemplate(): string {
  return `import { createFileRoute } from "@tanstack/react-router"
import { useBackendQuery, useBackendMutation } from "../declarations/backend"
import { useAuth } from "../lib/auth"

export const Route = createFileRoute("/")({
  component: HomeComponent,
})

function HomeComponent() {
  const { isAuthenticated, login, logout, principal } = useAuth()
  const { data: count } = useBackendQuery({
    functionName: "get_count",
  })
  const increment = useBackendMutation({
    functionName: "increment",
  })
  const reset = useBackendMutation({
    functionName: "reset",
  })

  return (
    <>
      <h1>IC Reactor Start</h1>
      <p>A fully on-chain React app on the Internet Computer.</p>

      <p>
        {isAuthenticated ? (
          <>
            <span>Authenticated as {principal?.toText()}</span>{" "}
            <button className="secondary" onClick={() => logout()}>
              Logout
            </button>
          </>
        ) : (
          <button className="secondary" onClick={() => login()}>
            Login with Internet Identity
          </button>
        )}
      </p>

      <p>Backend counter: <strong>{count?.toString() ?? "…"}</strong></p>

      <p>
        <button
          onClick={() => increment.mutate({ args: [] })}
          disabled={increment.isPending}
        >
          Increment
        </button>{" "}
        <button
          className="secondary"
          onClick={() => reset.mutate({ args: [] })}
          disabled={reset.isPending}
        >
          Reset
        </button>
      </p>
    </>
  )
}
`
}

