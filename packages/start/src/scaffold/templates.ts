/**
 * `frontend/src/routeTree.gen.ts` — committed seed so `tsc` passes before the
 * TanStack Router plugin runs. Regenerated on dev/build by the router plugin.
 */
export function frontendRouteTreeGenTemplate(): string {
  return `/* eslint-disable */
// @ts-nocheck

// This file is automatically generated by TanStack Router.
// Do not edit by hand — it is regenerated on "vite dev" / "vite build".
// Run "pnpm dev" once to populate it from src/routes.

import { Route as rootRouteImport } from "./routes/__root"
import { Route as IndexRouteImport } from "./routes/index"

const IndexRoute = IndexRouteImport.update({
  id: "/",
  path: "/",
  getParentRoute: () => rootRouteImport,
} as any)

export const routeTree = rootRouteImport.addChildren({
  IndexRoute,
} as any)
`
}

