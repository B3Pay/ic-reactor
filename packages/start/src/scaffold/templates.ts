/**
 * The complete ordered list of files the scaffold writes, as
 * `{ relativePath, content }` pairs. Keeping this declarative makes the
 * filesystem writer trivial and the scaffold test a simple assertion over the
 * list.
 */
export interface ScaffoldFile {
  relativePath: string
  content: string
}

/**
 * Build the full set of files for a new app. `appName` is validated by the
 * caller (`createApp`); templates only substitute it where needed.
 */
export function buildScaffoldFiles(ctx: TemplateContext): ScaffoldFile[] {
  return [
    { relativePath: "icp.yaml", content: icpYamlTemplate() },
    { relativePath: "ic-reactor.json", content: icReactorJsonTemplate() },
    { relativePath: ".gitignore", content: gitignoreTemplate() },
    { relativePath: "README.md", content: readmeTemplate(ctx) },
    {
      relativePath: "backend/canister.yaml",
      content: backendCanisterYamlTemplate(),
    },
    { relativePath: "backend/main.mo", content: backendMainMoTemplate() },
    { relativePath: "backend/backend.did", content: backendDidTemplate() },
    {
      relativePath: "frontend/canister.yaml",
      content: frontendCanisterYamlTemplate(),
    },
    {
      relativePath: "frontend/package.json",
      content: frontendPackageJsonTemplate(ctx),
    },
    {
      relativePath: "frontend/vite.config.ts",
      content: frontendViteConfigTemplate(),
    },
    {
      relativePath: "frontend/tsconfig.json",
      content: frontendTsconfigTemplate(),
    },
    {
      relativePath: "frontend/index.html",
      content: frontendIndexHtmlTemplate(ctx),
    },
    {
      relativePath: "frontend/src/main.tsx",
      content: frontendMainTsxTemplate(),
    },
    {
      relativePath: "frontend/src/styles.css",
      content: frontendStylesCssTemplate(),
    },
    {
      relativePath: "frontend/src/routeTree.gen.ts",
      content: frontendRouteTreeGenTemplate(),
    },
    {
      relativePath: "frontend/src/routes/__root.tsx",
      content: frontendRootRouteTemplate(),
    },
    {
      relativePath: "frontend/src/routes/index.tsx",
      content: frontendIndexRouteTemplate(),
    },
    {
      relativePath: "frontend/src/lib/client.ts",
      content: frontendClientTsTemplate(),
    },
    {
      relativePath: "frontend/src/lib/auth.ts",
      content: frontendAuthTsTemplate(),
    },
  ]
}
